package com.example.ezshop;

public class Product {
    public int id;
    public String name;
    public int qty;
    public String description;
    public String image;

    public Product(int id, String name, int qty, String description, String image) {
        this.id = id;
        this.name = name;
        this.qty = qty;
        this.description = description;
        this.image = image;
    }
}
